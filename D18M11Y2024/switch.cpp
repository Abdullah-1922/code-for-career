#include<iostream>

using namespace std;
int main(){
    int day;
    cout<<"Enter the day number: ";
    cin>>day;
    switch(day)
    {
        case 1:
        cout<<"Monday";
        case 2:
        cout<<"Tuesday";
        case 3:
        cout<<"Wednesday";
        case 4:
        cout<<"Thursday";

    }
    return 0;
}